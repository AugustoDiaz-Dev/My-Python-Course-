#Topic: Lists.

countries = ["Argentina", "Russia", "USA", "China", "Germany", "Japan"]

print(countries)

print(countries[0])
print(countries[-1])
print(countries[2:]) #Is going to start from index 2 (USA).
print(countries[1:4]) #Only gives the values between those two indexes.
countries[2] = "Canada" #Changes the value to a new one.

print(countries)
